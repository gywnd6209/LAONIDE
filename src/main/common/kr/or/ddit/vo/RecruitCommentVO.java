package kr.or.ddit.vo;

public class RecruitCommentVO {
	private String rnum;
	private String comment_no;
	private String bo_no;
	private String comment_writer;
	private String comment_reg_date;
	private String comment_group;
	private String comment_content;
	private String comment_seq;
	private String comment_depth;
	private String comment_status;
	

	public String getRnum() {
		return rnum;
	}
	public void setRnum(String rnum) {
		this.rnum = rnum;
	}
	public String getComment_no() {
		return comment_no;
	}
	public void setComment_no(String comment_no) {
		this.comment_no = comment_no;
	}
	public String getBo_no() {
		return bo_no;
	}
	public void setBo_no(String bo_no) {
		this.bo_no = bo_no;
	}
	public String getComment_writer() {
		return comment_writer;
	}
	public void setComment_writer(String comment_writer) {
		this.comment_writer = comment_writer;
	}
	public String getComment_reg_date() {
		return comment_reg_date;
	}
	public void setComment_reg_date(String comment_reg_date) {
		this.comment_reg_date = comment_reg_date;
	}
	public String getComment_group() {
		return comment_group;
	}
	public void setComment_group(String comment_group) {
		this.comment_group = comment_group;
	}
	public String getComment_content() {
		return comment_content;
	}
	public void setComment_content(String comment_content) {
		this.comment_content = comment_content;
	}
	public String getComment_seq() {
		return comment_seq;
	}
	public void setComment_seq(String comment_seq) {
		this.comment_seq = comment_seq;
	}
	public String getComment_depth() {
		return comment_depth;
	}
	public void setComment_depth(String comment_depth) {
		this.comment_depth = comment_depth;
	}
	public String getComment_status() {
		return comment_status;
	}
	public void setComment_status(String comment_status) {
		this.comment_status = comment_status;
	}
}
